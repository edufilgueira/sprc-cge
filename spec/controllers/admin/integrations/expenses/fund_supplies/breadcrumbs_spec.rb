require 'rails_helper'

describe Admin::Integrations::Expenses::FundSuppliesController  do

  let(:user) { create(:user, :admin) }
  let(:fund_supply) { create(:integration_expenses_fund_supply) }


  context 'index' do
    before { sign_in(user) && get(:index) }

    it 'breadcrumbs' do
      expected = [
        { title: I18n.t('admin.home.index.title'), url: admin_root_path },
        { title: I18n.t('admin.integrations.index.title'), url: admin_integrations_root_path },
        { title: I18n.t('admin.integrations.expenses.index.title'), url: admin_integrations_expenses_root_path },
        { title: I18n.t('admin.integrations.expenses.fund_supplies.index.title'), url: '' }
      ]

      expect(controller.breadcrumbs).to eq(expected)
    end
  end

  context 'show' do
    before { get(:show, params: { id: fund_supply }) }

    it 'breadcrumbs' do
      expected = [
        { title: I18n.t('admin.home.index.title'), url: admin_root_path },
        { title: I18n.t('admin.integrations.index.title'), url: admin_integrations_root_path },
        { title: I18n.t('admin.integrations.expenses.index.title'), url: admin_integrations_expenses_root_path },
        { title: I18n.t('admin.integrations.expenses.fund_supplies.index.title'), url: admin_integrations_expenses_fund_supplies_path },
        { title: fund_supply.title, url: '' }
      ]

      expect(controller.breadcrumbs).to eq(expected)
    end
  end
end

